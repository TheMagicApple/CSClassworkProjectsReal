
public class Cone {

	public static void main(String[] args) {
		double volume;
		double radius=3;
		double pi=3.14;
		double height=6;
		volume=pi*radius*radius*(height/3.0);
		System.out.println("Radius:"+radius);
		System.out.println("Pi:"+pi);
		System.out.println("Height:"+height);
		System.out.println("Volume:"+volume);

	}

}
